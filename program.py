# список задач по датам
tasks = {

}

def add_todo(date, task):
    """Добавляет задачу на указанную дату."""
    tasks.setdefault(date, []).append(task)
    print(f"Задача '{task}' добавлена на дату {date}.")

def show_tasks(date):
    """Показывает задачи на выбранную дату."""
    if date in tasks:
        if tasks[date]:
            for task in tasks[date]:
                print('-', task)
        else:
            print("На эту дату задач нет.")
    else:
        print("Такой даты нет.")

def show_all_tasks():
    """Показывает все задачи по всем датам."""
    if not tasks:
        print("Список задач пуст.")
        return
    for date, task_list in tasks.items():
        print(f"{date}:")
        for task in task_list:
            print('  -', task)

def add_random_task():
    """Добавляет случайную задачу на выбранную дату."""
    date = input("Введите дату для добавления случайного места: ")
    task = random.choice(RANDOM_TASKS)
    add_todo(date, task)

def main():
    while True:
        command = input("Введите команду: ").strip().lower()
        if command == "help":
            print(HELP)
        elif command == "show":
            date = input("Введите дату для отображения списка задач: ")
            show_tasks(date)
        elif command == "showall":
            show_all_tasks()
        elif command == "add":
            date = input("Введите дату для добавления задачи: ")
            task = input("Введите название задачи: ")
            add_todo(date, task)
        elif command == "random":
            add_random_task()
        elif command == "exit":
            print("До свидания!")
            break
        else:
            print("Неизвестная команда! Введите 'help' для справки.")